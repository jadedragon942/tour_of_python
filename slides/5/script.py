# For loop with range
print("Counting to 5:")
for i in range(5):
    print(i)

# For loop with list
print("\nLooping through fruits:")
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# While loop
print("\nWhile loop countdown:")
count = 3
while count > 0:
    print(count)
    count -= 1
